let age = 25;

//loose comparision (different types can still be equal)

console.log(age == 25);
console.log(age == '25');
console.log(age != 25);
console.log(age != '25');

// strict comparision (different types cannor be equal)

console.log(age === 25);
console.log (age === '25');
console.log(age !== 25);
console.log( age !== '25');